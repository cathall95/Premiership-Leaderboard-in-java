public class Results
{
  public int fixturesNumber;
  public int homeScore;
  public int awayScore;
  public int homeNumber;
  public int awayNumber;
  public void setFixtureNumber(int fixture)
  {
  fixturesNumber = fixture;
  }
  public void setHomeScore(int homescore)
  {
  homeScore = homescore;
  }
  public void setAwayScore(int awayscore)
  {
  awayScore = awayscore;
  }
  public void setHomeNumber(int homeNum)
  {
  homeNumber = homeNum;
  }
  public void setAwayNumber(int awayNum)
  {
  awayNumber = awayNum;
  }
  public int getFixturesNumber()
  {
  return fixturesNumber;
  }
  public int getHomeScore()
  {
  return homeScore;
  }
  public int getAwayScore()
  {
  return awayScore;
  }
  public int getHomeNumber()
  {
  return homeNumber;
  }
  public int getAwayNumber()
  {
  return awayNumber;
  }
}